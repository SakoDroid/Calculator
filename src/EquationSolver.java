    private void hitFinder(double La,double Lb,double Qa,double Qb,double Qc){
        ArrayList<Double> Xs = new ArrayList();
        ArrayList<Double> Ys = new ArrayList();
        for (double i = -350 ; i <= 350 ; i += 0.001){
            double y1 = La*i + Lb;
            double y2 = Qa*i*i + Qb*i + Qc;
            //System.out.printf("%f , %f , %f\n",y1,y2,i);
            if(Double.compare(y1, y2)==0){
                Xs.add(i);
                Ys.add(y1);
            }
        }
        if(Xs.isEmpty()){
            PtFd.setText("There are no hit points!\n----------------------\nSorry!");
        }else{
            String points = "";
            for (int i = 0; i < Xs.size(); i++) {
                points += String.format("%f , %f", Xs.get(i),Ys.get(i));
            }
            PtFd.setText("We found " + Xs.size()  + " points!\n" + "----------------------\n" + points);
        }
    }
