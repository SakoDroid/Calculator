    private double linearEquation(double a,double b){
        return -b/a;
    }
    private String quadraticEQuation(double a,double b,double c){
        double delta = b*b - (4*a*c);
        String ans = "";
        if(delta > 0){
           double x1 = (-b + Math.sqrt(delta))/(2*a);
           double x2 = (-b - Math.sqrt(delta))/(2*a);
           ans = String.format("Equation has two answers ==>    x1 = %s , x2 = %s",df.format(x1),df.format(x2));
        }else if (delta == 0){
            double x = -b/2*a;
            ans = String.format("Equation has one answer ==>    x = %s", df.format(x));
        }else{
            ans = "Equation has no answer!";
        }
        
        return ans;
    }
    private void linearPointFinder(double a,double b){
        prgs.setString("Finding and adding the points");
        if(fcp.getFirstTime()){
            fcp.setFirstTime(false);
        }
        for (double i=-350 ; i <=350 ; i += 0.001){
            prgs.setValue((int)i/28000);
            double y = a*i + b;
            fcp.addPoint((int)i, (int)y);
        }
        fcp.repaint();
    }
    private void quadraticPointFinder(double a,double b,double c){
        prgs.setString("Finding and adding the points");
        if(fcp.getFirstTime()){
            fcp.setFirstTime(false);
        }
        for (double i=-350 ; i <=350 ; i += 0.001) {
            prgs.setValue((int)i/14000);
            double y = a*i*i + b*i + c;
            fcp.addPoint((int)i, (int)y);
        }
        fcp.repaint();
    }
